package com.hidayatasep.makersinstitute.belajarcustomlist;

public class Word {

    String englishWord;
    String indonesianWord;

    public Word(String englishWord, String indonesianWord) {
        this.englishWord = englishWord;
        this.indonesianWord = indonesianWord;
    }

    public String getEnglishWord() {
        return englishWord;
    }

    public void setEnglishWord(String englishWord) {
        this.englishWord = englishWord;
    }

    public String getIndonesianWord() {
        return indonesianWord;
    }

    public void setIndonesianWord(String indonesianWord) {
        this.indonesianWord = indonesianWord;
    }
}



